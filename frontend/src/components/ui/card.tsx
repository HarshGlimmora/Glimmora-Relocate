import * as React from "react";
import { cn } from "@/lib/utils";

export const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { tone?: "paper" | "canvas" | "accent" }
>(({ className, tone = "canvas", ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "relative border",
      tone === "canvas" && "bg-canvas border-rule",
      tone === "paper" && "bg-paper-soft border-rule",
      tone === "accent" && "bg-accent-soft border-accent/15",
      className
    )}
    {...props}
  />
));
Card.displayName = "Card";

